#include "libft.h"

int	ft_strcmp_lex(const char *s1, const char *s2)
{
	int i;

	if (s1 == NULL)
		return (0 - s2[0]);
	if (s2 == NULL)
		return (s1[0]);
	i = 0;
	while ((s1[i] == s2[i]) && (s1[i] != '\0' || (s2[i] != '\0')))
		i++;
	return (s1[i] - s2[i]);
}
