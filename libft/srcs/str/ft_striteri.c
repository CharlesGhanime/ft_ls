#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t i;

	if (f == NULL || s == NULL)
		return ;
	i = 0;
	while (*s != '\0')
		(*f)(i++, s++);
}
